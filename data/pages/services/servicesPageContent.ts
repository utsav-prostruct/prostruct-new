function mergeProjectDetails({
  base,
  override,
}: {
  base: ServicesPageContent["projectDetails"];
  override?: WpServicesPageData["add_project_details"];
}) {
  if (!override?.length) return base;

  const map = new Map(
    override
      .filter(
        (
          item
        ): item is {
          project_heading: string;
          project_description: string;
          section_id?: string;
        } => Boolean(item.project_heading && item.project_description?.trim())
      )
      .map((item) => [item.project_heading.trim(), item])
  );

  return base.map((detail) => {
    const match = map.get(detail.heading);
    if (!match) return detail;
    return {
      ...detail,
      id: match.section_id?.trim() || detail.id,
      descriptionHtml: match.project_description,
    };
  });
}

function applyStepOverrides(
  overrideSteps: WpServicesPageData["steps"]
): ServicesPageContent["steps"] {
  if (!overrideSteps?.length) {
    return HOW_IT_WORKS_STEPS;
  }

  return HOW_IT_WORKS_STEPS.map((step, index) => {
    const override = overrideSteps[index];
    if (!override?.step_details?.trim()) {
      return step;
    }

    return {
      ...step,
      detailsHtml: override.step_details,
    };
  });
}

